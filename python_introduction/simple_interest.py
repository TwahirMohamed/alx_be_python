# A python script that calculates simple interest earned on an investment over a period of time.

principal = 1000 # principal amount invested
rate = 0.05 # annual interest rate for the investment of 5%
time = 3 # number of years in the investment

# Calculating the simple interest
interest = principal * rate * time

# Displaying the amount of interest earned
print(f"The simple interest is: {interest}")