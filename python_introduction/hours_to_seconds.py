# A python script that converts a specific number of hours into seconds.
# variable names are as follows: hours = 2
# remember there are 3600 seconds in an hour 
# Store results in variable named seconds
# print the result in format: [hours] hour(s) is [seconds] second(s)
# Example: 2 hour(s) is 7200 second(s)
hours = 2
seconds = hours * 3600
print(f"{hours} hour(s) is {seconds} second(s)")